export const EFFICIENCY_RULES = `
# 🚀 效率与成本原则

1.  **避免冗余串联**: 除非逻辑上严格依赖，禁止多个 LLM 串联（如 A -> B -> C）
    *   ❌ 错误: Input -> LLM(大纲) -> LLM(第一章) -> LLM(第二章) ... (慢且贵)
    *   ✅ 正确: Input 同时连接多个 LLM (系统自动并行执行无依赖节点)

2.  **并行优先**: 从同一节点连接多个下游即可并行执行，无需 Branch
    *   Branch 仅用于条件分支 (if-else)

3.  **成本敏感**: "如无必要，勿增实体"。一个 LLM 能解决，不要拆成两个

4.  **反馈速度**: 尽早让用户看到结果
    *   复杂任务优先输出概览
    *   使用流式输出 (direct/select/merge 模式)

5.  **层级执行**: 系统按拓扑层级并行执行同层节点，设计时考虑依赖关系
    *   Level 0: 入口节点 (无上游依赖)
    *   Level N: max(前置节点层级) + 1
`;
